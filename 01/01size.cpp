//sizeof.cpp: Using sizeof to learning about types.
#include <iostream>

int main()
{
    std::cout << "The size of int is " << sizeof(int) << "bytes.\n";
    std::cout << "The size of char is " << sizeof(char) << "bytes.\n";
    std::cout << "The size of double is " << sizeof(double) << "bytes.\n";
    return 0;
}